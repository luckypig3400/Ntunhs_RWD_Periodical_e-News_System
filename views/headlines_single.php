                  校慶當日在學思樓前舉行學思樓及爾雅樓二棟大樓落成啟用典禮，由臺北市立大學醒獅團的祥獅獻瑞演出及剪綵儀式後正式啟用。「學思樓」之命名取其「學而不思則罔，思而不學則殆」之意，殷盼全校師生同仁在學習過程中應學思並重，方能洞察事情的本質與真相。「爾雅樓」之命名其寓意在於本校昔年興建表彰女性溫婉德性之「蘭心樓」與「蕙質樓」，然隨著本校男生與日俱增，亦冀望其秉性溫文儒雅為人表率，成為健康照護產業的典範與中堅。<br><br>
                  校慶當週學生會舉辦一系列「74號夢想道路」任務活動，學生透過錄製影片與社團多元化展演，祝福北護生日快樂。當日慶祝大會由原住民文化交流社領唱校歌及體適能有氧社的熱情舞蹈揭開慶典的序幕，並藉由「守護北護~熱愛生命響應無菸拒毒暨遵守交通安全」宣導活動、「HealthyU~
